CREATE TABLE energy_production.productions (
    production_id INT PRIMARY KEY,                -- Unique identifier for each production record.
    energy_source_id INT,                         -- Reference to energy_sources table.
    date DATE,                                    -- Date of energy production.
    energy_produced DECIMAL(15,2),                -- Amount of energy produced in kilowatt-hours (kWh).
    FOREIGN KEY (energy_source_id) REFERENCES energy_sources.energy_source(energy_source_id)  -- Foreign key reference to energy_sources table.
);

